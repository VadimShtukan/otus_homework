package vadim.shtukan.otus.architect.finelproject.Document.Models;

public class Company {
    private String id;
    private String egrpo;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEgrpo() {
        return egrpo;
    }

    public void setEgrpo(String egrpo) {
        this.egrpo = egrpo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
